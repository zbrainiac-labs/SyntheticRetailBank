SELECT
 h.AS_OF_DATE,
 h.HOLDING_ID,
 h.ASSET_TYPE,
 e.ASSET_NAME,
 e.REGULATORY_LEVEL,
 h.ISIN,
 h.SECURITY_NAME,
 h.CURRENCY,
 h.QUANTITY,
 h.MARKET_VALUE_CCY,
 h.MARKET_VALUE_CHF,
 e.HAIRCUT_PCT,
 e.HAIRCUT_FACTOR,
 h.MARKET_VALUE_CHF * e.HAIRCUT_FACTOR AS WEIGHTED_VALUE_CHF,
 h.MATURITY_DATE,
 h.CREDIT_RATING,
 h.SMI_CONSTITUENT,
 h.HQLA_ELIGIBLE,
 h.INELIGIBILITY_REASON,
 h.PORTFOLIO_CODE,
