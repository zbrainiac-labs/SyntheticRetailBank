 L2A_COUNT NUMBER(10,0),
 L2B_COUNT NUMBER(10,0),
 OUTFLOW_RETAIL NUMBER(18,2),
 OUTFLOW_CORP NUMBER(18,2),
 OUTFLOW_FI NUMBER(18,2),
 OUTFLOW_TOTAL NUMBER(18,2),
 RETAIL_ACCOUNTS NUMBER(10,0),
 CORP_ACCOUNTS NUMBER(10,0),
 FI_ACCOUNTS NUMBER(10,0)
