 ASSET_TYPE VARCHAR(50),
 HOLDINGS_COUNT NUMBER(10,0),
 COUNTERPARTY_TYPE VARCHAR(50),
 ACCOUNT_COUNT NUMBER(10,0),
 L1_TOTAL NUMBER(18,2),
 L2A_TOTAL NUMBER(18,2),
 L2B_TOTAL NUMBER(18,2),
